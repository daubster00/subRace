import { getApiBucket } from './api-bucket';

// лӢӨмқҢ л§ҲмқјмҠӨнҶӨ = нҳ„мһ¬ api-bucketмқҳ мғҒлӢЁ кІҪкі„(= лӢӨмқҢ bucketмқҳ н•ҳлӢЁ).
//   <10M  вҶ’ 1л§Ң лӢЁмң„ (75_492_345 вҶ’ 75_500_000)
//   <100M вҶ’ 10л§Ң лӢЁмң„ (75_400_000 вҶ’ 75_500_000)
//   вүҘ100M вҶ’ 100л§Ң лӢЁмң„ (123_400_000 вҶ’ 124_000_000)
// API к°’мқҙ мқҙлҜё кІҪкі„м—җ м •нҷ•нһҲ кұёміҗ мһҲм–ҙлҸ„(75_500_000) лӢӨмқҢ л§ҲмқјмҠӨнҶӨмқҖ
// к·ё лӢӨмқҢ bucket(75_600_000)мқ„ л°ҳнҷҳн•ңлӢӨ вҖ” нҳ„мһ¬ к°’мқҖ "лҸ„лӢ¬н•ң" л§ҲмқјмҠӨнҶӨмқҙлҜҖлЎң.
export function getNextMilestone(apiCount: number): number {
  return getApiBucket(apiCount).ceilExclusive;
}
